#!/usr/bin/env node
/**
 * ensure-theme-overlays.mjs
 *
 * Creates empty but valid overlay stubs if they don't exist.
 * Run as `predev` and `prebuild` so fresh clones build without custom-themes/.
 * Idempotent — no-op when files already exist.
 */
import { existsSync, writeFileSync, mkdirSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const docsRoot = resolve(__dirname, "..");

const overlays = [
  {
    path: resolve(docsRoot, "app", "custom-themes.generated.css"),
    content:
      "/* generated by `visor theme sync` — empty when no custom themes are present */\n",
  },
  {
    path: resolve(docsRoot, "lib", "theme-config.custom.generated.ts"),
    content:
      'import type { ThemeGroup } from "./theme-config";\nexport const customThemeGroups: ThemeGroup[] = [];\n',
  },
  {
    path: resolve(docsRoot, "app", "private-themes.generated.css"),
    content:
      "/* generated by scripts/generate-private-themes.mjs — empty when @low-orbit-studio/visor-themes-private is not installed */\n",
  },
  {
    path: resolve(docsRoot, "lib", "private-themes.generated.ts"),
    content:
      'import type { PrivateThemeEntry } from "./private-themes";\nexport const PRIVATE_THEMES: PrivateThemeEntry[] = [];\n',
  },
];

for (const { path, content } of overlays) {
  if (!existsSync(path)) {
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, content, "utf-8");
    console.log(`[ensure-theme-overlays] created stub: ${path}`);
  }
}
