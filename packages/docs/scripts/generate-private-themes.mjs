#!/usr/bin/env node
/**
 * generate-private-themes.mjs
 *
 * Build-time generator that materializes per-theme CSS and a manifest from
 * `@low-orbit-studio/visor-themes-private`. Runs as `predev` and `prebuild`.
 *
 * When the package is installed (consumer has GITHUB_PACKAGES_TOKEN with
 * read:packages access), iterates `listThemes()` and emits:
 *   - app/private-themes.generated.css   — one block per theme, scoped to `.{slug}-theme`
 *   - lib/private-themes.generated.ts    — `export const PRIVATE_THEMES = [...]`
 *
 * When the package is not installed (public self-hosters, no token), writes
 * empty stubs so the build continues. The /themes/private route reads the
 * empty manifest and returns 404.
 *
 * Idempotent — safe to run repeatedly.
 */
import { existsSync, readFileSync, writeFileSync, mkdirSync } from "node:fs"
import { resolve, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const docsRoot = resolve(__dirname, "..")
const cssOutPath = resolve(docsRoot, "app", "private-themes.generated.css")
const tsOutPath = resolve(docsRoot, "lib", "private-themes.generated.ts")

const EMPTY_CSS = "/* generated by scripts/generate-private-themes.mjs — empty when @low-orbit-studio/visor-themes-private is not installed */\n"
const EMPTY_TS = `import type { PrivateThemeEntry } from "./private-themes";\nexport const PRIVATE_THEMES: PrivateThemeEntry[] = [];\n`

function toLabel(slug) {
  return slug
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ")
}

function writeEmpty(reason) {
  mkdirSync(dirname(cssOutPath), { recursive: true })
  mkdirSync(dirname(tsOutPath), { recursive: true })
  writeFileSync(cssOutPath, EMPTY_CSS, "utf-8")
  writeFileSync(tsOutPath, EMPTY_TS, "utf-8")
  console.log(`[generate-private-themes] empty stubs written (${reason})`)
}

async function main() {
  let pkg
  try {
    pkg = await import("@low-orbit-studio/visor-themes-private")
  } catch {
    writeEmpty("package not installed")
    return
  }

  let engine, adapters
  try {
    engine = await import("@loworbitstudio/visor-theme-engine")
    adapters = await import("@loworbitstudio/visor-theme-engine/adapters")
  } catch (err) {
    console.error(`[generate-private-themes] theme-engine import failed: ${err.message}`)
    process.exit(1)
  }

  const slugs = pkg.listThemes()
  if (slugs.length === 0) {
    writeEmpty("package installed but exports zero themes")
    return
  }

  const cssBlocks = []
  const manifest = []
  const importLines = new Set()

  for (const slug of slugs) {
    let yamlContent
    try {
      yamlContent = pkg.readTheme(slug)
    } catch (err) {
      console.error(`[generate-private-themes] readTheme(${slug}) failed: ${err.message}`)
      process.exit(1)
    }

    let metaPath, meta
    try {
      const themeFilePath = pkg.themePath(slug)
      metaPath = resolve(dirname(themeFilePath), "meta.json")
      meta = JSON.parse(readFileSync(metaPath, "utf-8"))
    } catch (err) {
      console.error(`[generate-private-themes] meta.json read failed for ${slug}: ${err.message}`)
      process.exit(1)
    }

    let themeData
    try {
      themeData = engine.generateThemeData(yamlContent)
    } catch (err) {
      console.error(`[generate-private-themes] generateThemeData failed for ${slug}: ${err.message}`)
      process.exit(1)
    }

    const css = adapters.docsAdapter({
      primitives: themeData.primitives,
      tokens: themeData.tokens,
      config: themeData.config,
    })

    // Hoist @import rules — PostCSS requires them at the top of the file.
    // The pattern matches a full line that starts with `@import` and ends with
    // a trailing `;` — robust against semicolons embedded in URL query strings.
    const cssWithoutImports = css.replace(/^\s*@import\s+.+;\s*$/gm, (match) => {
      importLines.add(match.trim())
      return ""
    })

    cssBlocks.push(`/* ===== ${slug} ===== */\n${cssWithoutImports.trim()}\n`)

    manifest.push({
      slug,
      label: toLabel(slug),
      group: meta.group ?? "Private",
    })
  }

  manifest.sort((a, b) => {
    if (a.group !== b.group) return a.group.localeCompare(b.group)
    return a.slug.localeCompare(b.slug)
  })

  const importHeader = [...importLines].sort().join("\n")
  const cssOut = `/* generated by scripts/generate-private-themes.mjs — do not edit manually */\n${importHeader ? `${importHeader}\n` : ""}\n${cssBlocks.join("\n\n")}\n`

  const manifestLiteral = manifest
    .map((e) => `  { slug: ${JSON.stringify(e.slug)}, label: ${JSON.stringify(e.label)}, group: ${JSON.stringify(e.group)} },`)
    .join("\n")
  const tsOut = `import type { PrivateThemeEntry } from "./private-themes";\n// generated by scripts/generate-private-themes.mjs — do not edit manually\nexport const PRIVATE_THEMES: PrivateThemeEntry[] = [\n${manifestLiteral}\n];\n`

  mkdirSync(dirname(cssOutPath), { recursive: true })
  mkdirSync(dirname(tsOutPath), { recursive: true })
  writeFileSync(cssOutPath, cssOut, "utf-8")
  writeFileSync(tsOutPath, tsOut, "utf-8")
  console.log(`[generate-private-themes] generated ${manifest.length} theme(s): ${manifest.map((e) => e.slug).join(", ")}`)
}

main().catch((err) => {
  console.error(`[generate-private-themes] unexpected error: ${err.stack ?? err.message}`)
  process.exit(1)
})
