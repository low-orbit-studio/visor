function generatePrimitivesCSS(): string {
  const lines: string[] = [];

  // Colors
  lines.push(sectionComment("Primitive: Colors"));
  const colorDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveColors)) {
    colorDecls.push(`--color-${name}: ${value};`);
  }
  lines.push(block(":root", colorDecls));

  // Spacing
  lines.push(sectionComment("Primitive: Spacing"));
  const spacingDecls: string[] = [];
  for (const [name, px] of Object.entries(primitiveSpacing)) {
    const rem = px === 0 ? "0" : `${px / 16}rem`;
    spacingDecls.push(`--spacing-${name}: ${rem}; /* ${px}px */`);
  }
  lines.push(block(":root", spacingDecls));

  // Border Radius
  lines.push(sectionComment("Primitive: Border Radius"));
  const radiusDecls: string[] = [];
  for (const [name, px] of Object.entries(primitiveRadius)) {
    if (px >= 9999) {
      radiusDecls.push(`--radius-${name}: 9999px;`);
    } else if (px === 0) {
      radiusDecls.push(`--radius-${name}: 0;`);
    } else {
      radiusDecls.push(`--radius-${name}: ${px / 16}rem; /* ${px}px */`);
    }
  }
  lines.push(block(":root", radiusDecls));

  // Border Widths
  lines.push(sectionComment("Primitive: Border Widths"));
  const borderWidthDecls: string[] = [];
  for (const [name, px] of Object.entries(primitiveBorderWidths)) {
    borderWidthDecls.push(`--border-width-${name}: ${px}px;`);
  }
  lines.push(block(":root", borderWidthDecls));

  // Font Families
  lines.push(sectionComment("Primitive: Font Families"));
  const fontFamilyDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveFontFamilies)) {
    fontFamilyDecls.push(`--font-${name}: ${value};`);
  }
  lines.push(block(":root", fontFamilyDecls));

  // Font Sizes
  lines.push(sectionComment("Primitive: Font Sizes"));
  const fontSizeDecls: string[] = [];
  for (const [name, px] of Object.entries(primitiveFontSizes)) {
    fontSizeDecls.push(`--font-size-${name}: ${px / 16}rem; /* ${px}px */`);
  }
  lines.push(block(":root", fontSizeDecls));

  // Font Weights
  lines.push(sectionComment("Primitive: Font Weights"));
  const fontWeightDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveFontWeights)) {
    fontWeightDecls.push(`--font-weight-${name}: ${value};`);
  }
  lines.push(block(":root", fontWeightDecls));

  // Line Heights
  lines.push(sectionComment("Primitive: Line Heights"));
  const lineHeightDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveLineHeights)) {
    lineHeightDecls.push(`--line-height-${name}: ${value};`);
  }
  lines.push(block(":root", lineHeightDecls));

  // Shadows
  lines.push(sectionComment("Primitive: Shadows"));
  const shadowDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveShadows)) {
    shadowDecls.push(`--shadow-${name}: ${value};`);
  }
  lines.push(block(":root", shadowDecls));

  // Z-index
  lines.push(sectionComment("Primitive: Z-Index"));
  const zIndexDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveZIndex)) {
    zIndexDecls.push(`--z-${name}: ${value};`);
  }
  lines.push(block(":root", zIndexDecls));

  // Overlay
  lines.push(sectionComment("Primitive: Overlay"));
  const overlayDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveOverlay)) {
    overlayDecls.push(`--overlay-${name}: ${value};`);
  }
  lines.push(block(":root", overlayDecls));

  // Focus Ring
  lines.push(sectionComment("Primitive: Focus Ring"));
  const focusRingDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveFocusRing)) {
    focusRingDecls.push(`--focus-ring-${name}: ${value};`);
  }
  lines.push(block(":root", focusRingDecls));

  // Motion Durations
  lines.push(sectionComment("Primitive: Motion Durations"));
  const motionDurationDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveMotionDurations)) {
    motionDurationDecls.push(`--motion-duration-${name}: ${value};`);
  }
  lines.push(block(":root", motionDurationDecls));

  // Motion Easings
  lines.push(sectionComment("Primitive: Motion Easings"));
  const motionEasingDecls: string[] = [];
  for (const [name, value] of Object.entries(primitiveMotionEasings)) {
    motionEasingDecls.push(`--motion-easing-${name}: ${value};`);
  }
  lines.push(block(":root", motionEasingDecls));

  return header("Visor Design Tokens — Primitives") + lines.join("\n");
}

