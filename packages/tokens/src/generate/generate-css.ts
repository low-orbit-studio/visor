/** Build adaptive token declarations for a given theme variant */
function buildAdaptiveDecls(theme: "light" | "dark"): {
  textDecls: string[];
  surfaceDecls: string[];
  borderDecls: string[];
  interactiveDecls: string[];
  skeletonDecls: string[];
  shadowDecls: string[];
  chartDecls: string[];
  sidebarDecls: string[];
} {
  const textDecls: string[] = [];
  for (const [name, values] of Object.entries(adaptiveText)) {
    textDecls.push(`--text-${name}: ${toVar(values[theme])};`);
  }

  const surfaceDecls: string[] = [];
  for (const [name, values] of Object.entries(adaptiveSurface)) {
    surfaceDecls.push(`--surface-${name}: ${toVar(values[theme])};`);
  }

  const borderDecls: string[] = [];
  for (const [name, values] of Object.entries(adaptiveBorder)) {
    borderDecls.push(`--border-${name}: ${toVar(values[theme])};`);
  }

  const interactiveDecls: string[] = [];
  for (const [name, values] of Object.entries(adaptiveInteractive)) {
    interactiveDecls.push(`--interactive-${name}: ${toVar(values[theme])};`);
  }

  const skeletonDecls: string[] = [];
  for (const [name, values] of Object.entries(adaptiveSkeleton)) {
    skeletonDecls.push(`--skeleton-${name}: ${toVar(values[theme])};`);
  }

  // Shadows use raw values (not var() references) so they override per-theme correctly
  const shadowSource = theme === "dark" ? primitiveShadowsDark : primitiveShadows;
  const shadowDecls: string[] = [];
  for (const [name, value] of Object.entries(shadowSource)) {
    shadowDecls.push(`--shadow-${name}: ${value};`);
  }

  const chartDecls: string[] = [];
  for (const [name, values] of Object.entries(adaptiveChart)) {
    chartDecls.push(`--chart-${name}: ${toVar(values[theme])};`);
  }

  const sidebarDecls: string[] = [];
  for (const [name, values] of Object.entries(adaptiveSidebar)) {
    sidebarDecls.push(`--sidebar-${name}: ${toVar(values[theme])};`);
  }

  return { textDecls, surfaceDecls, borderDecls, interactiveDecls, skeletonDecls, shadowDecls, chartDecls, sidebarDecls };
}

function generateThemeCSS(theme: "light" | "dark"): string {
  const themeLabel = theme === "light" ? "Light Theme" : "Dark Theme";
  const lines: string[] = [];
  const { textDecls, surfaceDecls, borderDecls, interactiveDecls, skeletonDecls, shadowDecls, chartDecls, sidebarDecls } = buildAdaptiveDecls(theme);

  if (theme === "light") {
    // Light theme: applied to :root (default)
    lines.push(sectionComment("Adaptive: Text (light)"));
    lines.push(block(":root", textDecls));

    lines.push(sectionComment("Adaptive: Surface (light)"));
    lines.push(block(":root", surfaceDecls));

    lines.push(sectionComment("Adaptive: Border (light)"));
    lines.push(block(":root", borderDecls));

    lines.push(sectionComment("Adaptive: Interactive (light)"));
    lines.push(block(":root", interactiveDecls));

    lines.push(sectionComment("Adaptive: Skeleton (light)"));
    lines.push(block(":root", skeletonDecls));

    lines.push(sectionComment("Adaptive: Shadows (light)"));
    lines.push(block(":root", shadowDecls));

    lines.push(sectionComment("Adaptive: Chart (light)"));
    lines.push(block(":root", chartDecls));

    lines.push(sectionComment("Adaptive: Sidebar (light)"));
    lines.push(block(":root", sidebarDecls));
  } else {
    // Dark theme: applied to manual selectors
    const darkSelectors = [
      ".dark",
      ".theme-dark",
      '[data-theme="dark"]',
    ];
    const darkSelector = darkSelectors.join(",\n");

    lines.push(sectionComment("Adaptive: Text (dark) — manual toggle"));
    lines.push(block(darkSelector, textDecls));

    lines.push(sectionComment("Adaptive: Surface (dark) — manual toggle"));
    lines.push(block(darkSelector, surfaceDecls));

    lines.push(sectionComment("Adaptive: Border (dark) — manual toggle"));
    lines.push(block(darkSelector, borderDecls));

    lines.push(sectionComment("Adaptive: Interactive (dark) — manual toggle"));
    lines.push(block(darkSelector, interactiveDecls));

    lines.push(sectionComment("Adaptive: Skeleton (dark) — manual toggle"));
    lines.push(block(darkSelector, skeletonDecls));

    lines.push(sectionComment("Adaptive: Shadows (dark) — manual toggle"));
    lines.push(block(darkSelector, shadowDecls));

    lines.push(sectionComment("Adaptive: Chart (dark) — manual toggle"));
    lines.push(block(darkSelector, chartDecls));

    lines.push(sectionComment("Adaptive: Sidebar (dark) — manual toggle"));
    lines.push(block(darkSelector, sidebarDecls));

    // prefers-color-scheme: dark — auto detection
    const mediaRoot = ":root:not(.light):not(.theme-light):not([data-theme=\"light\"])";

    lines.push(sectionComment("Adaptive: Text (dark) — prefers-color-scheme"));
    lines.push(`@media (prefers-color-scheme: dark) {\n${block(mediaRoot, textDecls)}}`);
    lines.push("");

    lines.push(sectionComment("Adaptive: Surface (dark) — prefers-color-scheme"));
    lines.push(`@media (prefers-color-scheme: dark) {\n${block(mediaRoot, surfaceDecls)}}`);
    lines.push("");

    lines.push(sectionComment("Adaptive: Border (dark) — prefers-color-scheme"));
    lines.push(`@media (prefers-color-scheme: dark) {\n${block(mediaRoot, borderDecls)}}`);
    lines.push("");

    lines.push(sectionComment("Adaptive: Interactive (dark) — prefers-color-scheme"));
    lines.push(`@media (prefers-color-scheme: dark) {\n${block(mediaRoot, interactiveDecls)}}`);
    lines.push("");

    lines.push(sectionComment("Adaptive: Skeleton (dark) — prefers-color-scheme"));
    lines.push(`@media (prefers-color-scheme: dark) {\n${block(mediaRoot, skeletonDecls)}}`);
    lines.push("");

    lines.push(sectionComment("Adaptive: Shadows (dark) — prefers-color-scheme"));
    lines.push(`@media (prefers-color-scheme: dark) {\n${block(mediaRoot, shadowDecls)}}`);
    lines.push("");

    lines.push(sectionComment("Adaptive: Chart (dark) — prefers-color-scheme"));
    lines.push(`@media (prefers-color-scheme: dark) {\n${block(mediaRoot, chartDecls)}}`);
    lines.push("");

    lines.push(sectionComment("Adaptive: Sidebar (dark) — prefers-color-scheme"));
    lines.push(`@media (prefers-color-scheme: dark) {\n${block(mediaRoot, sidebarDecls)}}`);
    lines.push("");
  }

  return header(`Visor Design Tokens — ${themeLabel}`) + lines.join("\n");
}

// ============================================================
// Full bundle (tokens.css)
// ============================================================

function generateTokensCSS(): string {
  const lines: string[] = [
    header("Visor Design Tokens — Full Bundle"),
    "/* Import order: primitives → semantic → adaptive */",
    "",
    "/* ============================================",
    "   Tier 1: Primitives",
    "   ============================================ */",
  ];

  // Primitives inlined
  const primitivesContent = generatePrimitivesCSS();
  // Strip the header from the primitives content (first 6 lines are header)
  const primitivesBody = primitivesContent
    .split("\n")
    .slice(6)
    .join("\n");
  lines.push(primitivesBody);

  lines.push(
    "/* ============================================",
    "   Tier 2: Semantic",
    "   ============================================ */"
  );
  const semanticContent = generateSemanticCSS();
  const semanticBody = semanticContent.split("\n").slice(6).join("\n");
  lines.push(semanticBody);

  lines.push(
    "/* ============================================",
    "   Tier 3: Adaptive — Light Theme (:root)",
    "   ============================================ */"
  );
  const lightContent = generateThemeCSS("light");
  const lightBody = lightContent.split("\n").slice(6).join("\n");
  lines.push(lightBody);

  lines.push(
    "/* ============================================",
    "   Tier 3: Adaptive — Dark Theme (.dark, .theme-dark, [data-theme=\"dark\"])",
    "   and @media (prefers-color-scheme: dark)",
    "   ============================================ */"
  );
  const darkContent = generateThemeCSS("dark");
  const darkBody = darkContent.split("\n").slice(6).join("\n");
  lines.push(darkBody);

  lines.push(
    "/* ============================================",
    "   Global: Motion Safety",
    "   ============================================ */"
  );
  lines.push(
    "@media (prefers-reduced-motion: reduce) {",
    "  *, *::before, *::after {",
    "    animation-duration: 0.01ms !important;",
    "    animation-iteration-count: 1 !important;",
    "    transition-duration: 0.01ms !important;",
    "  }",
    "}",
    ""
  );

  return lines.join("\n");
}

// ============================================================
// Index CSS (entry point)
// ============================================================

function generateIndexCSS(): string {
  return [
    header("Visor Design Tokens — Index"),
    "/* Full design token bundle */",
    "@import './tokens.css';",
    "",
  ].join("\n");
}

// ============================================================
// Stock theme CSS generation
// ============================================================

function generateStockThemes(): void {
  for (const slug of STOCK_THEMES) {
    const yamlPath = join(STOCK_THEMES_DIR, `${slug}.visor.yaml`);
    if (!existsSync(yamlPath)) {
      throw new Error(`Stock theme YAML not found: ${yamlPath}`);
    }
    const yaml = readFileSync(yamlPath, "utf-8");
    const data = generateThemeData(yaml);
    const css = docsAdapter({ primitives: data.primitives, tokens: data.tokens, config: data.config });
    writeFileSync(join(THEMES_DIR, `${slug}.css`), css, "utf-8");
    console.log(`✓ dist/themes/${slug}.css`);
  }
}

